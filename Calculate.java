package Calculate;

public class Calculate {
	public static void main(String[] args){
		System.out.println(6/4);
		System.out.println(6%4);
		
		int i=8;							
		int j=i++ +2;				//i++和i相等
		int k=8;
		int m=++k +2;				//++k等于k+1
		
		System.out.println(j);
		System.out.println(m);
	}
}
